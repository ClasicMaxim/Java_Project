public class StringJava {
    public static void checkStringEquality() {
        // Объявление переменных
        String name = "Максим";
        String secondName = "Максим";
        String thirdName = "Максим";
        // Вывод результата сравнения строк
        System.out.println(name.equals(secondName)); // сравниваем содержимое строк
        System.out.println(name.equals(thirdName)); // сравниваем содержимое строк
    }
}
